let a = 3, b = 2;
let z = a+b;
console.log("Resultado de la suma: " + z);

let z1 = a-b;
console.log("Resultado de la resta: " + z1);

let z2 = a*b;
console.log("Resultado de la multiplicacion: " + z2);

let z3 = a/b;
console.log("Resultado de la division: " + z3);

let z4 = a**b;
console.log("Resultado de la exponente: " + z4);

let z5 = a%b;
console.log("Resultado del modulo o residuo: " + z5);

// incremento
// preincremento (el operador ++ antes de la variable)
let z6 = ++a;
console.log(a)
console.log(z6);
// poost - incremento (el operador ++ despues de la variable)
let z7 = b++;
console.log(b);
console.log(z7);
//pre - decremento 
let z8 = --a;
console.log(a);
console.log(z8);
//post decremento
let z9 = b--;
console.log(b);
console.log(z9);
