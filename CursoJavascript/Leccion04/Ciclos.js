let contador = 0;
/**************************************** */ 
console.log("Ciclo while");
while( contador < 3 ){ 
    console.log(contador);
    contador++; 
}
/**************************************** */
console.log("Ciclo do while")
contador = 0; 
do{
    console.log(contador); 
    contador++; 
}while( contador < 3); 
/**************************************** */
console.log("Ciclo for")
for( let contador = 0; contador < 3; contador++){
console.log(contador)

}

